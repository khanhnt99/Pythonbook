# format dung de dinh dang chuoi
thu_tu_mac_dinh ="{},{} va {}".format('Quan','Tri','Mang')
print('\n---Thu tu mac dinh---')
print(thu_tu_mac_dinh)

vi_tri_thu_tu='{1},{0} va {2}'.format('Quan','Tri','Mang')
print('\n---Thu tu theo vi tri---')
print(vi_tri_thu_tu)

tu_khoa_thu_tu='{s},{b},{j}'.format(j='Quan',b='Tri',s='Mang')
print('\n---Thu tu theo tu khoa---')
print(tu_khoa_thu_tu)

